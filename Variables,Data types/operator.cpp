#include<iostream>
using namespace std;

int main() 
{

    int a = 5, b= 10;
    int sum = a + b; // Addition operator
    cout << "Sum: " << sum << endl; // Output the sum
    
    int difference = b - a; // Subtraction operator
    cout << "Difference: " << difference << endl; // Output the difference
    
    int product = a * b; // Multiplication operator
    cout << "Product: " << product << endl; // Output the product
    
    int quotient = b / a; // Division operator
    cout << "Quotient: " << quotient << endl; // Output the quotient
    
    int ans = (5 / (double)2);
    cout << (5 / (double)2) << endl;
    cout << "Answer: " << ans << endl; // Output the answer

    return 0; // Return statement to indicate successful execution


}